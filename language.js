class LanguageManager {
    constructor() {
        const savedLang = localStorage.getItem('preferredLang');
        
        const browserLang = navigator.language.split('-')[0];
        
        const supportedLangs = ['pt', 'en', 'es', 'zh'];
        
        this.currentLang = savedLang || (supportedLangs.includes(browserLang) ? browserLang : 'en');
        
        this.init();
    }

    init() {
        this.applyTranslation(this.currentLang);
        this.bindEvents();
    }

    bindEvents() {
        const options = document.querySelectorAll('.lang-dropdown li');
        options.forEach(opt => {
            opt.addEventListener('click', () => {
                const lang = opt.getAttribute('data-lang');
                if (lang) this.changeLanguage(lang);
            });
        });
    }

    changeLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('preferredLang', lang);
        this.applyTranslation(lang);
    }

    applyTranslation(lang) {
        const texts = languages[lang];
        if (!texts) return;

        document.documentElement.lang = texts.html_lang;

        const label = document.getElementById('current-lang-label');
        if (label) {
            label.innerText = texts.label;
            label.className = 'lang-' + lang;
        }

        const heroTitle = document.querySelector('[data-i18n="hero_title"]');
        if (heroTitle) {
            heroTitle.innerHTML = texts.hero_title;
        }

        document.querySelectorAll('[data-i18n]:not([data-i18n="hero_title"])').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (key && texts[key]) {
                el.innerText = texts[key];
            }
        });

        document.querySelectorAll('.lang-dropdown li').forEach(li => {
            li.classList.toggle('active', li.getAttribute('data-lang') === lang);
        });
    }
}

document.addEventListener('DOMContentLoaded', () => {
    new LanguageManager();
});